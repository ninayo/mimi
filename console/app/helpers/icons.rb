  def self.raw(icon, classes)
    "<i class='#{icon}#{(classes ? ' ' + classes : '')}'></i>".html_safe
  end

  def self.i(target, classes: nil)
    "<i class='#{self.icon(target)}#{(classes ? ' ' + classes : '')}'></i>".html_safe
  end

  def self.s(target, classes: nil)
    "<small><i class='#{self.icon(target)}#{(classes ? ' ' + classes : '')}'></i></small>".html_safe
  end

  def self.fw(target)
    "<i class='fa-fw #{self.icon(target)}'></i>".html_safe
  end

  def self.icon(target)
    ICONS[target.downcase.to_sym]
  end
  
  def self.method_missing(icon)
    ICONS[icon]
  end
  
  def self.failed(_failed)
    "<i class='fa-fw #{self.failed_classes(_failed)}'></i>".html_safe
  end

  def self.failed_classes(_failed)
    ICONS[(_failed ? :failed : :succeeded)]
  end

  def self.toggle(on)
    "<i class='#{ICONS[:toggle][on.to_s]}'></i>".html_safe
  end

